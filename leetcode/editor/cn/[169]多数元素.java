class Solution {
    public int majorityElement(int[] nums) {
        int target = nums[0];
        int weight = 1;

        for (int i = 1; i < nums.length; i++) {
            int cur = nums[i];
            if (weight == 0) {
                target = cur;
                weight++;
            } else if (target == cur) {
                weight++;
            } else {
                weight--;
            }
        }

        weight = 0;
        for (int i = 0; i < nums.length; i++) {
            if (nums[i] == target) {
                weight++;
            }
        }
        if (weight * 2 > nums.length) {
            return target;
        }
        return Integer.MIN_VALUE;
    }
}
